+ (void)logSportsTeamJSON {
	NSString *sportsJSON = [self sportsTeamsColorsJSON];
	NSError *error;
	NSDictionary *sportsDictionary = [NSDictionary dictionaryWithDictionary:[NSJSONSerialization JSONObjectWithData:[sportsJSON dataUsingEncoding:NSStringEncodingConversionAllowLossy] options:NSJSONReadingAllowFragments error:&error]];
	
	NSMutableString *finalOutputString = [[NSMutableString alloc] init];
	
	for (NSString *key in sportsDictionary.allKeys) {
		NSArray *leagueJSONArray = [sportsDictionary objectForKey:key];
		NSArray *leagueArray = [NSArray arrayWithArray:leagueJSONArray];
		
		[finalOutputString appendFormat:@"#pragma mark - %@\n\n", [key uppercaseString]];
		
		for (NSDictionary *teamObject in leagueArray) {
			NSDictionary *teamDictionary = [NSDictionary dictionaryWithDictionary:teamObject];
			
			NSString *teamName;
			NSString *shortenedTeamName;
			
			for (NSString *colorKey in teamDictionary.allKeys) {
				NSObject *colorObject = [teamDictionary objectForKey:colorKey];
				
				if ([[[colorObject class] description] isEqualToString:@"__NSCFString"]) {
					NSString *potentialTeamName = (NSString *)colorObject;
					
					if ([potentialTeamName rangeOfString:@".com"].location == NSNotFound) {
						teamName = (NSString *)colorObject;
						
						NSLog(@"Team Name: %@", colorObject);
						
						NSArray *words = [teamName componentsSeparatedByCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
						
						if (words.count > 0) {
							if ([[words lastObject] length] > 3) {
								shortenedTeamName = [words lastObject];
								
								if ([finalOutputString rangeOfString:shortenedTeamName].location != NSNotFound) {
									if (words.count > 1) {
										for (NSInteger i = words.count - 2; i >= 0; i--) {
											NSString *word = [words objectAtIndex:i];
											shortenedTeamName = [NSString stringWithFormat:@"%@%@", [word substringToIndex:1], shortenedTeamName];
										}
									}
								}
							}
							
							shortenedTeamName = [shortenedTeamName lowercaseString];
							
							if ([shortenedTeamName isEqualToString:teamName]) {
								shortenedTeamName = nil;
							}
						}
					}
					
					
				} else if ([[[colorObject class] description] isEqualToString:@"__NSCFDictionary"]) {
					NSDictionary *colorDictionary = [NSDictionary dictionaryWithDictionary:(NSDictionary *)colorObject];
					
					if ([colorDictionary objectForKey:@"hex"]) {
						NSArray *hexArray = [NSArray arrayWithArray:[colorDictionary objectForKey:@"hex"]];
						
						if (teamName) {
							
							NSInteger colorCount = 1;
							for (NSString *hexString in hexArray) {
								[finalOutputString appendFormat:@"\n+ (NKFColor *)%@%@ {\n\treturn [NKFColor colorWithHexString:@\"#%@\"];\n}\n", [[[teamName lowercaseString] componentsSeparatedByCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]] componentsJoinedByString:@""], (colorCount > 1) ? [NSString stringWithFormat:@"%zd", colorCount] : @"", hexString];
								colorCount++;
							}
							
							if (shortenedTeamName) {
								colorCount = 1;
								for (int j = 0; j < hexArray.count; j++) {
									[finalOutputString appendFormat:@"\n+ (NKFColor *)%@%@ {\n\treturn [NKFColor %@%@];\n}\n", [[[shortenedTeamName lowercaseString] componentsSeparatedByCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]] componentsJoinedByString:@""], (colorCount > 1) ? [NSString stringWithFormat:@"%zd", colorCount] : @"", [[[teamName lowercaseString] componentsSeparatedByCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]] componentsJoinedByString:@""], (colorCount > 1) ? [NSString stringWithFormat:@"%zd", colorCount] : @""];
									colorCount++;
								}
							}
							
						} else {
							NSLog(@"No team name yet!");
						}
					} else {
						NSLog(@"No key for hex: %@", colorDictionary.allKeys);
					}
				}
			}
			
			[finalOutputString appendString:@"\n"];
		}
	}
	
	NSLog(@"+ (void)logSportsTeamJSON: %@", finalOutputString);
}

+ (void)logCompanyColorsDictionary {
	NSString *companiesColorsString = [JSONColorFormatter companiesColorsObjectiveC];
	
	NSString *delimeter = @"+ (NKFColor *)";
	
	NSArray *components = [companiesColorsString componentsSeparatedByString:delimeter];
	
	
	NSDictionary *basicColors = @{@"black" : @"#000000",
								  @"white" : @"#FFFFFF",
								  @"red"   : @"#FF0000",
								  @"bible" : @"#BA3355",
                                  @"forestGreenTraditional" : @"#004521"};
	
	NSMutableDictionary *colorKeys = [[NSMutableDictionary alloc] initWithDictionary:basicColors];
	
	
	for (NSString *substring in components) {
		NSString *key = substring;
		
		NSRange openBraceRange = [substring rangeOfString:@"{"];
		if (openBraceRange.location != NSNotFound) {
			NSString *titleString = [[substring substringToIndex:openBraceRange.location] stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
			
			if (titleString) {
				key = titleString;
			}
		} else {
			DLog(@"No open brace found.");
		}
		
		NSRange hexRange = [substring rangeOfString:@"@\"#"];
		
		if (hexRange.location != NSNotFound) {
			if (substring.length >= hexRange.length + 2 + 7) {
				NSString *hexString = [substring substringWithRange:NSMakeRange(hexRange.location + 2, 7)];
				
				[colorKeys setValue:hexString forKey:key];
			}
		} else {
			NSRange colorWithRange = [substring rangeOfString:@"colorWith"];
			
			if (colorWithRange.location != NSNotFound) {
				NSString *rgbColorString = [substring substringFromIndex:colorWithRange.location + 9];
				
				if ([rgbColorString containsString:@"White"]) {
					NSArray *whiteComponents = [rgbColorString componentsSeparatedByString:@".0f/255.0f"];
					
					NSString *whiteHexString = [[whiteComponents firstObject] substringFromIndex:[[whiteComponents firstObject] rangeOfString:@":"].location + 1];
					
					if (whiteHexString.length > 2) {
						whiteHexString = [whiteHexString substringToIndex:2];
					}
					
					NSString *outputString = [NSString stringWithFormat:@"#%@%@%@", whiteHexString, whiteHexString, whiteHexString];
					
					[colorKeys setObject:outputString forKey:key];
				} else {
                    NSString *denominatorString = @".0f/255.0f";
                    NSString *red, *green, *blue;

                    if ([rgbColorString containsString:denominatorString]) {
                        NSArray *rgbComponents = [rgbColorString componentsSeparatedByString:denominatorString];
                        
                        for (int i = 0; i < rgbComponents.count && i < 3; i++) {
                            NSString *currentComponentString = [rgbComponents objectAtIndex:i];
                            
                            NSString *subComponent = [currentComponentString substringFromIndex:[currentComponentString rangeOfString:@":"].location + 1];
                            
                            if (!red) {
                                red  = [NSString stringWithFormat:@"%02X", [subComponent intValue]];
                            } else if (!green) {
                                green = [NSString stringWithFormat:@"%02X", [subComponent intValue]];
                            } else if (!blue) {
                                blue = [NSString stringWithFormat:@"%02X", [subComponent intValue]];
                            }
                        }
                    } else if ([rgbColorString containsString:@"f "]) {
                        NSArray *rgbComponents = [rgbColorString componentsSeparatedByString:@" f"];
                        
                        for (int i = 0; i < rgbComponents.count && i < 3; i++) {
                            NSString *currentComponentString = [rgbComponents objectAtIndex:i];
                            
                            NSString *subComponent = [currentComponentString substringFromIndex:[currentComponentString rangeOfString:@":"].location + 1];
                            
                            if (!red) {
                                red  = [NSString stringWithFormat:@"%02X", (int)([subComponent intValue] * 255.0f)];
                            } else if (!green) {
                                green = [NSString stringWithFormat:@"%02X", (int)([subComponent intValue] * 255.0f)];
                            } else if (!blue) {
                                blue = [NSString stringWithFormat:@"%02X", (int)([subComponent intValue] * 255.0f)];
                            }
                        }
                    }
                    
                    
                    if (!red || ! green || !blue) {
                        if (red && !green) {
                            green = red;
                            blue = red;
                            // just create a color with white...but as red?
                        } else {
                            NSLog(@"Missing Something for %@ \tr: %@\tg: %@\tb:%@", key, red, green, blue);
                        }
                    }
                    
                    NSString *outputString = [NSString stringWithFormat:@"#%@%@%@", red, green, blue];
                    
                    [colorKeys setValue:outputString forKey:key];

				}
			} else {
				NSRange rangeOfDefaultColor = [substring rangeOfString:@"[NKFColor "];
				
				if (rangeOfDefaultColor.location != NSNotFound) {
					NSString *remainderString = [substring substringFromIndex:rangeOfDefaultColor.location + rangeOfDefaultColor.length];
					NSRange lastBracketRange = [remainderString rangeOfString:@"]"];
					
					if (lastBracketRange.location != NSNotFound) {
						NSString *otherColorString = [remainderString substringToIndex:lastBracketRange.location];
						
						[colorKeys setObject:otherColorString forKey:key];
						
					} else {
						NSLog(@"Whoops...I screwed up: %@", remainderString);
					}
				} else {
					NSLog(@"I don't know what's going on: %@", substring);
				}
			}
		}
	}
	
	NSArray *keys = [NSArray arrayWithArray:colorKeys.allKeys];
	
	for (NSString *key in keys) {
		NSString *hexString = [colorKeys objectForKey:key];
		if (![hexString containsString:@"#"]) {
			NSString *tempKey = [colorKeys objectForKey:hexString];
			
			while ([colorKeys objectForKey:tempKey]) {
				tempKey = [colorKeys objectForKey:tempKey];
			}
			
			if (tempKey) {
				[colorKeys setObject:tempKey forKey:key];
			} else if ([colorKeys objectForKey:hexString]) {
				[colorKeys setObject:[colorKeys objectForKey:hexString] forKey:key];
			} else if ([NKFColor colorWithName:hexString]) {
				[colorKeys setObject:[[NKFColor colorWithName:hexString] hexValue] forKey:key];
            } else {
				NSLog(@"%@\t: %@", key, hexString);
			}
		}
	}
	
	for (NSString *key in basicColors) {
		[colorKeys removeObjectForKey:key];
	}
	
	NSMutableString *outputString = [[NSMutableString alloc] initWithString:@"\n\n@{\n"];
	
	NSInteger longestKeyLength = 0;
	
	for (NSString *key in colorKeys.allKeys) {
		if (key.length > longestKeyLength) {
			longestKeyLength = key.length;
		}
	}
	
	NSString *space = @"    ";
	
	NSArray *alphabetizedArray = [colorKeys.allKeys sortedArrayUsingSelector:@selector(localizedCaseInsensitiveCompare:)];
	
	for (NSString *key in alphabetizedArray) {
		NSMutableString *lineEntry = [[NSMutableString alloc] initWithString:space];
		
		[lineEntry appendFormat:@"@\"%@\"", key];
		
		for (NSInteger i = key.length; i < longestKeyLength; i++) {
			[lineEntry appendString:@" "];
		}
		
		[lineEntry appendString:@" : "];
		
		NSString *hexString = [colorKeys objectForKey:key];
		
		if (hexString.length == 6 && ![hexString containsString:@"#"]) {
			hexString = [NSString stringWithFormat:@"#%@", hexString];
		}
		
		[lineEntry appendFormat:@"@\"%@\",\n", [hexString uppercaseString]];
		
		[outputString appendString:lineEntry];
	}
	
	[outputString appendString:@"}\n\n"];
	
	NSLog(@"%@", outputString);
	
	
}

